/* The following methods are used to initialize Prometheus histogram vectors for gRPC connections. */

type LabelNames struct {
	Service string
	Method  string
	Code    string
}

// ToList is useful for defining label names when creating a new Prometheus vector
func (l LabelNames) ToList() []string {
	return []string{l.Code, l.Service, l.Method}
}

func rangeProtofiles(m map[string][]string, fd protoreflect.FileDescriptor) bool {
	if fd.Services().Len() == 0 {
		return true
	}

	for i := 0; i < fd.Services().Len(); i++ {
		s := fd.Services().Get(i)
		if s.Methods().Len() == 0 {
			continue
		}

		methods := []string{}
		for j := 0; j < s.Methods().Len(); j++ {
			methods = append(methods, string(s.Methods().Get(j).Name()))
		}
		m[string(s.FullName())] = methods
	}

	return true
}

var allGrpcCodes = []codes.Code{
	codes.OK, codes.InvalidArgument, codes.PermissionDenied,
	codes.FailedPrecondition,

	// Codes which can be generated by the gRPC framework
	codes.Canceled, codes.Unknown, codes.DeadlineExceeded,
	codes.ResourceExhausted, codes.Unimplemented, codes.Internal,
	codes.Unavailable, codes.Unauthenticated,
}

// InitializeGrpcCollectorsFromPackage registers and zeroes a Prometheus histogram, populating all service and method labels
// by ranging through a given protobuf package.
func InitializeGrpcCollectorsFromPackage(r prometheus.Registerer, v prometheus.ObserverVec, l LabelNames, pkg protoreflect.FileDescriptor) {
	if r == nil {
		return
	}
	r.MustRegister(v)

	serviceNamesToMethodNames := make(map[string][]string, 0)
	protoregistry.GlobalFiles.RangeFilesByPackage(
		pkg.Package(),
		func(fd protoreflect.FileDescriptor) bool { return rangeProtofiles(serviceNamesToMethodNames, fd) },
	)

	for serviceName, serviceMethods := range serviceNamesToMethodNames {
		for _, sm := range serviceMethods {
			for _, c := range allGrpcCodes {
				v.With(prometheus.Labels{
					l.Service: serviceName,
					l.Method:  sm,
					l.Code:    c.String(),
				})
			}
		}
	}
}

// InitializeGrpcCollectorsFromServer registers and zeroes a Prometheus histogram, finding all service and method labels
// from the provided gRPC server.
func InitializeGrpcCollectorsFromServer(r prometheus.Registerer, v prometheus.ObserverVec, l LabelNames, server *grpc.Server) {
	if r == nil {
		return
	}
	r.MustRegister(v)

	for serviceName, info := range server.GetServiceInfo() {
		for _, mInfo := range info.Methods {
			for _, c := range allGrpcCodes {
				v.With(prometheus.Labels{
					l.Service: serviceName,
					l.Method:  mInfo.Name,
					l.Code:    c.String(),
				})
			}
		}
	}
}

/* The following methods are used to instrument handlers for gRPC server and client connections. */

// statusFromError retrieves the *status.Status from the provided error.  It'll
// attempt to unwrap the *status.Error, which is something status.FromError
// does not do.
func statusFromError(err error) *status.Status {
	if s, ok := status.FromError(err); ok {
		return s
	}

	type gRPCStatus interface {
		GRPCStatus() *status.Status
	}
	var unwrappedStatus gRPCStatus
	if ok := errors.As(err, &unwrappedStatus); ok {
		return unwrappedStatus.GRPCStatus()
	}

	return status.New(codes.Unknown, "Unknown Code")
}

func splitMethodName(fullMethodName string) (string, string) {
	fullMethodName = strings.TrimPrefix(fullMethodName, "/") // remove leading slash
	if i := strings.Index(fullMethodName, "/"); i >= 0 {
		return fullMethodName[:i], fullMethodName[i+1:]
	}
	return "unknown", "unknown"
}

type metricMethodNameContextKey struct{}

type RPCStatsHandler struct {
	Metric prometheus.ObserverVec
	Labels LabelNames
}

func (sh RPCStatsHandler) TagRPC(ctx context.Context, i *stats.RPCTagInfo) context.Context {
	return context.WithValue(ctx, metricMethodNameContextKey{}, i.FullMethodName)
}

func (sh RPCStatsHandler) TagConn(ctx context.Context, _ *stats.ConnTagInfo) context.Context {
	return ctx
}

func (sh RPCStatsHandler) HandleConn(context.Context, stats.ConnStats) {
}

func (sh RPCStatsHandler) HandleRPC(ctx context.Context, s stats.RPCStats) {
	switch v := s.(type) {
	case *stats.End:
		// Accept the ok, but ignore it. This code doesn't need to panic
		// and if "fullName" is an empty string splitMethodName will
		// set service and method to "unknown".
		fullName, _ := ctx.Value(metricMethodNameContextKey{}).(string)
		service, method := splitMethodName(fullName)
		labels := prometheus.Labels{
			sh.Labels.Method:  method,
			sh.Labels.Service: service,
			sh.Labels.Code:    statusFromError(v.Error).Code().String(),
		}
		sh.Metric.With(labels).Observe(v.EndTime.Sub(v.BeginTime).Seconds())
	}
}

type requestRecorder struct {
	handler RPCStatsHandler
	labels  prometheus.Labels

	// measurements
	start time.Time
}

func newRequestRecorder(fullMethodName string, handler RPCStatsHandler) requestRecorder {
	service, method := splitMethodName(fullMethodName)
	r := requestRecorder{
		handler: handler,
		labels: prometheus.Labels{
			handler.Labels.Method:  method,
			handler.Labels.Service: service,
		},
		start: time.Now(),
	}

	return r
}

func (r requestRecorder) record(err error) {
	r.labels[r.handler.Labels.Code] = statusFromError(err).Code().String()
	r.handler.Metric.With(r.labels).Observe(time.Since(r.start).Seconds())
}

// InstrumentClusterClient wraps a UnaryClientInterceptor and records
// observations for the collectors associated with gRPC connections
// between the cluster and its clients.
func InstrumentClusterClient(sh RPCStatsHandler) grpc.UnaryClientInterceptor {
	return func(ctx context.Context, method string, req, reply interface{}, cc *grpc.ClientConn, invoker grpc.UnaryInvoker, opts ...grpc.CallOption) error {
		r := newRequestRecorder(method, sh)
		err := invoker(ctx, method, req, reply, cc, opts...)
		r.record(err)
		return err
	}
}
